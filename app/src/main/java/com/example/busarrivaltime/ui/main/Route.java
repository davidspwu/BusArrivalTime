package com.example.busarrivaltime.ui.main;

public class Route {
    public String mPhone;
    public String mBus;
    public String mStop;
    public String mDescription;

    public Route(String phone, String bus, String stop, String description) {
        mPhone = phone;
        mBus = bus;
        mStop = stop;
        mDescription = description;
    }
}
